let firstNumber = 18;
let secondNumber = 4;

console.log('18 + 4 = ', firstNumber + secondNumber);
console.log('18 - 4 = ', firstNumber - secondNumber);
console.log('4 - 18 = ', secondNumber - firstNumber);
console.log('18 * 4 = ', firstNumber * secondNumber);
console.log('18 / 4 = ', firstNumber / secondNumber);
console.log('18 % 4 = ', firstNumber % secondNumber);